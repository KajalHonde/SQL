SELECT * FROM PRODUCTS;

-- COUNT HOW MANY PRODUCTS EACH PRODUCTLINE HAS 
SELECT productLine, COUNT(productCode)
AS NUMBER_OF_PRODUCTS
FROM PRODUCTS
GROUP BY productLine
ORDER BY productLine;

-- SHOW ME PRODUCTLINE HAVEING MORE THAN 20 PRODUCTS
SELECT productLine, COUNT(productCode)
AS NUMBER_OF_PRODUCTS
FROM PRODUCTS
GROUP BY productLine
HAVING NUMBER_OF_PRODUCTS > 20 ;
-- ALIAS USED IN THE ABOVE QUERY

-- COUNT FUNCTION USED IN THE BELOW QUERY
SELECT productLine, COUNT(productCode)
AS NUMBER_OF_PRODUCTS
FROM PRODUCTS
GROUP BY productLine
HAVING COUNT(PRODUCTCODE) > 20 ;

-- SHOW OFFICES HAVINNG MORE THAN 4 EMPLOYEES
SELECT * FROM EMPLOYEES;
SELECT OFFICECODE, COUNT(EMPLOYEENUMBER)
AS TOTAL_EMPLOYEES
FROM EMPLOYEES
GROUP BY OFFICECODE
HAVING COUNT(EMPLOYEENUMBER) > 4 ;

-- SHOW COUNTRIES HAVING MORE THAN 5 CUSTOMERS;
SELECT * FROM CUSTOMERS;
SELECT COUNTRY, COUNT(customerNumber)
AS TOTAL_CUSTOMERS
FROM CUSTOMERS
GROUP BY  COUNTRY
HAVING COUNT(customerNumber) > 5 ;

-- SHOW CITIES HAVING CUSTOMER COUNT MORE THAN 3
SELECT CITY , COUNT(CUSTOMERNUMBER)
AS TOTAL_CUSTMORES
FROM CUSTOMERS 
GROUP BY CITY
HAVING COUNT(CUSTOMERNUMBER) > 2; 
